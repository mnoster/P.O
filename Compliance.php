        <h2>Patient rights</h2>
        <hr>
        <p>Under HIPAA, patients have the following rights:</p>

        <ol>
            <li>To access, copy and inspect their own health information;</li>
            <li>To request an amendment or correction to existing information;</li>
            <li>To collect an accounting of certain disclosures;</li>
            <li>To request restrictions on disclosures for treatment and/or payment of health care;</li>
            <li>To request alternative communications methods; and</li>
            <li>To complain about violations and/or a covered entity’s own policies.</li>
        </ol>
    </div>
