        <h2>Data safeguards</h2>
        <hr>
        <p>There are a number of things that a covered entity must do to safeguard Protected Health Information, some of
            which includes:</p>

        <ul>
            <li>Using a unique user ID for identifying and tracking (recording) user identity.</li>
            <li>Enabling automatic logoff procedures for computers that access and display Protected Health Information.
                The
                automatic logoff function ends a session upon detecting a predetermined amount of computer inactivity.
            </li>
            <li>Encrypting Protected Health Information</li>
            <li>Periodically checking for data tampering and/or unauthorized access to such data</li>
            <li>Implementing a data restoration (data recovery) program in the event of an emergency.</li>
            <li>Controlling access to Protected Health Information and the environment in which it’s housed (including
                computers and software programs).
            </li>
            <li>Documenting all employee, patient, and visitor activity related to the access, use, and/or disclosure of
                Protected Health Information.
            </li>
            <li>Establishing functions for all areas that contain PHI and ensuring those functions are performed.</li>
            <li>Monitoring and documenting the current location, removal of and final destination of all PHI</li>
            <li>Performing regular risk assessments, activity reviews, and sanctions (if required).</li>
        </ul>

        <p>The full scope of requirements is provided in the HIPAA Regulations section of this course.</p>
