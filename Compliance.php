        <h2>Research</h2>
        <hr>
        <p>When patient health information needs to be disclosed for research purposes, an agreement (informed consent
            document) must be signed by each participant and describe the study, anticipated risks and/or benefits, and
            the
            manner in which records will be protected. This agreement, too, must be written in plain language.</p>

        <h3>Limited data set</h3>
        <p>A limited data set is health information that excludes data that can be used to identify an individual. Data
            that
            can be used to identify an individual is called a “direct identifier.” Examples of direct identifiers are
            names,
            addresses, phone numbers, e-mail addresses, social security numbers, insurance numbers, and more. A limited
            data
            set only uses city, state, ZIP code data and other types of information that cannot be used to identify a
            patient.<sup>1</sup></p>

