        <h2>Purpose</h2>
        <p>PsychOrigins.com is a resource and tool for licensed clinicians as well those in the field of research.
            It provides a readily updated and organized documentation of current trends within the mental health field.
        </p>
        <p>A current problem in mental health research is that experiments are usually conducted in research institutions,
            such as universities. Furthermore, many experiments consist of voluntary participants and sometimes are compensated for their time, which
            can be a confounding factor. A great deal of data is missing from these representative samples, chiefly those who are actually affected
            by mental health conditions.
        </p>
        <p>With this application, we wish to appropriately aggregate information and use it to
            generate better information
            so that researchers, pharmaceutical companies, and curious minds have access to more representative data.
        </p>
